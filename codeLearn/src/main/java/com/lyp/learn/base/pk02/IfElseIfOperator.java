package com.lyp.learn.base.pk02;

public class IfElseIfOperator {
    public static void main(String[] args) {
        int score = 70;
        if(score >= 90){
            System.out.println("A");
        }else if(score >= 80){
            System.out.println("B");
        }else if(score >= 60){
            System.out.println("C");
        }else{
            System.out.println("D");
        }
    }
}
