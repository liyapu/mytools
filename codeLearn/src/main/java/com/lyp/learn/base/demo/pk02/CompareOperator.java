public class CompareOperator {
    public static void main(String[] args) {
        int a = 25;
        int b = 4;
        System.out.println("a > b 是 " + (a > b));
        System.out.println("a < b 是 " + (a < b));
        System.out.println("a == b 是 " + (a == b));
        System.out.println("a >= b 是 " + (a >= b));
        System.out.println("a <= b 是 " + (a <= b));
    }
}
