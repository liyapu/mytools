public class MathOperator {
    public static void main(String[] args) {
        int a = 25; //声明 int 类型变量 a ,并赋值为 25
        int b = 4;  //声明 int 类型变量 b ,并赋值为 4
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
    }
}
